import fs from 'node:fs'
import path from 'node:path'

const EBAS_ROOT = 'C:\\Projects\\EBAdb\\docs\\ebas'
const OUTPUT_DIR = 'C:\\Projects\\EBAdb\\docs\\.vitepress\\generated'
const OUTPUT_FILE = 'C:\\Projects\\EBAdb\\docs\\.vitepress\\generated\\topic-list.mjs'

function getAllMarkdownFiles(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true })
  const files = []

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name)

    if (entry.isDirectory()) {
      files.push(...getAllMarkdownFiles(fullPath))
      continue
    }

    if (entry.isFile() && fullPath.toLowerCase().endsWith('.md')) {
      files.push(fullPath)
    }
  }

  return files
}

function getFrontMatterBlock(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/)
  return match ? match[1] : null
}

function stripWrappingQuotes(value) {
  const trimmed = value.trim()

  if (
    (trimmed.startsWith("'") && trimmed.endsWith("'")) ||
    (trimmed.startsWith('"') && trimmed.endsWith('"'))
  ) {
    return trimmed.slice(1, -1).trim()
  }

  return trimmed
}

function parseInlineTopicArray(value) {
  const trimmed = value.trim()

  if (!trimmed.startsWith('[') || !trimmed.endsWith(']')) {
    return []
  }

  const inner = trimmed.slice(1, -1).trim()

  if (!inner) {
    return []
  }

  return inner
    .split(',')
    .map(part => stripWrappingQuotes(part))
    .filter(Boolean)
}

function getTopicsFromFrontMatter(frontMatter) {
  const lines = frontMatter.split(/\r?\n/)
  const topics = []
  let inTopicsBlock = false

  for (const line of lines) {
    if (!inTopicsBlock) {
      const inlineMatch = line.match(/^\s*topics\s*:\s*(.+?)\s*$/)

      if (inlineMatch) {
        const value = inlineMatch[1].trim()

        if (value) {
          return parseInlineTopicArray(value)
        }

        inTopicsBlock = true
      }

      continue
    }

    const itemMatch = line.match(/^\s*-\s*(.+?)\s*$/)

    if (itemMatch) {
      const topic = stripWrappingQuotes(itemMatch[1])

      if (topic) {
        topics.push(topic)
      }

      continue
    }

    if (/^\s*$/.test(line)) {
      continue
    }

    if (/^\S/.test(line) || /^[A-Za-z0-9_-]+\s*:/.test(line)) {
      break
    }

    break
  }

  return topics
}

function collectUniqueTopics() {
  const files = getAllMarkdownFiles(EBAS_ROOT)
  const topics = new Set()

  for (const file of files) {
    const raw = fs.readFileSync(file, 'utf8')
    const frontMatter = getFrontMatterBlock(raw)

    if (!frontMatter) {
      continue
    }

    for (const topic of getTopicsFromFrontMatter(frontMatter)) {
      topics.add(topic)
    }
  }

  return [...topics].sort((a, b) => a.localeCompare(b))
}

function writeGeneratedModule(topics) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true })

  const lines = [
    '// AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.',
    '// Generated by C:\\Projects\\EBAdb\\docs\\scripts\\Generate-TopicList.mjs',
    '',
    `export const topicList = ${JSON.stringify(topics, null, 2)}`,
    '',
    'export default topicList',
    ''
  ]

  fs.writeFileSync(OUTPUT_FILE, lines.join('\n'), 'utf8')
}

function main() {
  if (!fs.existsSync(EBAS_ROOT)) {
    throw new Error(`EBA content root not found: ${EBAS_ROOT}`)
  }

  const topics = collectUniqueTopics()
  writeGeneratedModule(topics)

  console.log(`Generated ${topics.length} topics`)
  console.log(OUTPUT_FILE)
}

main()